package com.example.orderfood.Model;

public class Staff {
    private int id, role;
    private String name, pass, gender, brithday;

    public Staff(int id, int role, String name, String pass, String gender, String brithday) {
        this.id = id;
        this.role = role;
        this.name = name;
        this.pass = pass;
        this.gender = gender;
        this.brithday = brithday;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBrithday() {
        return brithday;
    }

    public void setBrithday(String brithday) {
        this.brithday = brithday;
    }
}
